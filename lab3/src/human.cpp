#include "human.h"

void Human::setString(char*& dest, const char* src) {
    delete[] dest;
    if (src) {
        int len = std::strlen(src);
        dest = new char[len + 1];
        std::strncpy(dest, src, len);
        dest[len] = '\0';
    }
    else dest = nullptr;
}


